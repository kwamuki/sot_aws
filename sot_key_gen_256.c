/**
  *@brief		SoT 암호화키 생성 함수
  *@param		*DeviceKey 디바이스 인증키	
  *@param		DeviceKeyLen 디바이스 인증키 길이	
  *@param		*EncryptKey 생성된 암호화키를 저장할 참조 포인터
  *@param		EncryptKeyLen 생성할  암호화키의 길이 
  *
  * SoT crypto 프로젝트 소스파일
  *
  *@remark		디바이스 인증키를 매개변수로 하여 지정된 길이의 (EncryptKeyLen) 암호화 키를 생성
*/
void SOT_KeyGen(
		BYTE	*DeviceKey,
		DWORD	DeviceKeyLen,
		BYTE	*EncryptKey,
		BYTE	EncryptKeyLen)
{
	int i, j, c;
	BYTE RoundKey[KEY_SIZE];
	DWORD	start;
	size_t t;

	for(i=0; i<4 ; i++){
		RoundKey[i	] = DeviceKey[i+12];
		RoundKey[i+4] =	DeviceKey[i   ];
	}

	for (i=0; i<16 ;i++){
		for(j=0;j<8;j++)
			RoundKey[8+16*i+j	] = (BYTE)(DeviceKey[(j-i)%(DeviceKeyLen-1)	] + CArray[16*i+j]);
		for(j=0;j<8;j++)
			RoundKey[8+16*i+j+8] = (BYTE)(DeviceKey[((j-i)%(DeviceKeyLen-1))+8]+CArray[16*i+j+8]);
	}
	srand(time(NULL));
	start = rand()%(255-EncryptKeyLen);

	for (i=0; i<255 ;i++){
		if(start+i >= 255)
			start = 0;
		EncryptKey[i] = RoundKey[start+i];
	}
	RoundKey[0] = '\0';
}
